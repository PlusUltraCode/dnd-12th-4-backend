package com.dnd12th_4.pickitalki.common.config;

import io.swagger.v3.oas.models.OpenAPI;
import io.swagger.v3.oas.models.Components;
import io.swagger.v3.oas.models.info.Info;
import io.swagger.v3.oas.models.security.SecurityRequirement;
import io.swagger.v3.oas.models.security.SecurityScheme;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;

@Configuration
public class SwaggerConfig {

    @Bean
    public OpenAPI customOpenAPI() {
        return new OpenAPI()
                .info(new Info().title("PickiTalki API").version("1.0"))
                .components(new Components()
                        // üîπ Bearer Token Ïù∏Ï¶ù ÏÑ§Ï†ï
                        .addSecuritySchemes("Bearer Authentication",
                                new SecurityScheme()
                                        .type(SecurityScheme.Type.HTTP)
                                        .scheme("bearer")
                                        .bearerFormat("JWT"))
                        // üîπ Refresh Token Ïø†ÌÇ§ Ïù∏Ï¶ù ÏÑ§Ï†ï
                        .addSecuritySchemes("CookieAuth",
                                new SecurityScheme()
                                        .type(SecurityScheme.Type.APIKEY)  // Ïø†ÌÇ§ Í∏∞Î∞ò Ïù∏Ï¶ù
                                        .in(SecurityScheme.In.COOKIE)      // Ïø†ÌÇ§ÏóêÏÑú ÏùΩÏùå
                                        .name("refreshToken")              // Ïø†ÌÇ§ Ïù¥Î¶Ñ
                        ))
                .addSecurityItem(new SecurityRequirement()
                        .addList("Bearer Authentication")  // Bearer Ïù∏Ï¶ù Ï†ÅÏö©
                        .addList("CookieAuth"));           // RefreshToken Ïù∏Ï¶ù Ï†ÅÏö©
    }
}
